def _open_records(rows, family, shape):
    out = []
    for name, h, b, tw, tf, r, mass in rows:
        out.append(
            {
                "designation": name,
                "family": family,
                "shape": shape,
                "h": float(h),
                "b": float(b),
                "tw": float(tw),
                "tf": float(tf),
                "r": float(r),
                "mass_per_m": float(mass),
            }
        )
    return out


