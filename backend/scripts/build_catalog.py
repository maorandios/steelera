# --------------------------------------------------------------------------- #
# Cold-formed purlins / girts: Cee + Zed. Generic range (no single EN dim      #
# standard for cold-formed sections — spot-check against your supplier).       #
# Flange and lip scale with web depth. Mass from developed width x thickness.  #
# --------------------------------------------------------------------------- #
_CF_DEPTHS = [80, 100, 120, 140, 150, 170, 200, 230, 260, 300, 340]
_CF_THICKNESSES = [1.5, 2.0, 2.5, 3.0]


def _cf_flange_lip(depth: float) -> tuple[float, float]:
    """Representative flange width and lip length for a cold-formed depth (mm)."""
    flange = round(45.0 + 0.13 * depth)  # ~55 @120 .. ~89 @340
    lip = round(15.0 + 0.03 * depth)  # ~19 @120 .. ~25 @340
    return float(flange), float(lip)


def _cf_records(family: str, shape: str, prefix: str):
    """Cee or Zed sections. Developed width = web + 2*flange + 2*lip."""
    out = []
    for depth in _CF_DEPTHS:
        flange, lip = _cf_flange_lip(float(depth))
        for t in _CF_THICKNESSES:
            developed = depth + 2.0 * flange + 2.0 * lip
            area = developed * t
            out.append(
                {
                    "designation": f"{prefix}{depth:g}x{t:g}",
                    "family": family,
                    "shape": shape,
                    "h": float(depth),
                    "b": flange,
                    "tw": float(t),
                    "tf": float(t),
                    "t": float(t),
                    "lip": lip,
                    "mass_per_m": _mass_from_area(area),
                }
            )
    return out


